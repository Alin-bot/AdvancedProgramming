package com.compulsory.Lab11Compulsory.controllers;

public class Person {

    public String name;
    public int id;

    public Person( int id, String name) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }
}
